public class MultiplicationTableTJGE {
    public static void main(String[] args) {
        int num = 5;                            // declare int variable num with a value of 5

        /* for loop to print multiplication table of number 5 from 1 to 10*/
        for (int i = 1; i <= 10; i++) {
            int product = num * i;
            System.out.println(num + " x " + i + " = " + product);
        }
    }
}

